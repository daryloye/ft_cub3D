#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	char	last_digit;
	long	n;

	n = nb;
	if (n < 0)
	{
		write (fd, "-", 1);
		n = -n;
	}
	if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	else
	{
		last_digit = n + '0';
		write (fd, &last_digit, 1);
	}
}
