#include "libft.h"

int	print_s(char *str)
{
	if (!str)
	{
		ft_putstr_fd ("(null)", 1);
		return (6);
	}
	else
	{
		ft_putstr_fd (str, 1);
		return (ft_strlen(str));
	}
}
